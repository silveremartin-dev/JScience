package org.jscience.server.model;

import jakarta.persistence.*;
import java.io.Serializable;
import java.time.LocalDateTime;

/**
 * JPA Entity for tracking submitted Grid Jobs.
 */
@Entity
@Table(name = "jobs")
public class Job implements Serializable {

    @Id
    private String jobId; // UUID

    private String status; // QUEUED, RUNNING, COMPLETED, FAILED

    private String submittedBy; // Username

    private int priority;

    private LocalDateTime submittedAt;

    private LocalDateTime completedAt;

    public Job() {
    }

    public Job(String jobId, String status, String submittedBy, int priority) {
        this.jobId = jobId;
        this.status = status;
        this.submittedBy = submittedBy;
        this.priority = priority;
        this.submittedAt = LocalDateTime.now();
    }

    public String getJobId() {
        return jobId;
    }

    public void setJobId(String jobId) {
        this.jobId = jobId;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getSubmittedBy() {
        return submittedBy;
    }

    public void setSubmittedBy(String submittedBy) {
        this.submittedBy = submittedBy;
    }

    public int getPriority() {
        return priority;
    }

    public void setPriority(int priority) {
        this.priority = priority;
    }

    public LocalDateTime getSubmittedAt() {
        return submittedAt;
    }

    public void setSubmittedAt(LocalDateTime submittedAt) {
        this.submittedAt = submittedAt;
    }

    public LocalDateTime getCompletedAt() {
        return completedAt;
    }

    public void setCompletedAt(LocalDateTime completedAt) {
        this.completedAt = completedAt;
    }
}


